# First Name: Jessica
# Last Name: Aurora
def get_full_name_length(first_name, last_name):
    full_name = first_name + last_name
    return len(full_name)
# Prompt the user for their first and last names
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")
# Calculate the length of the full name
length = get_full_name_length(first_name, last_name)
# Display the result
print(f"The length of your name is {length} characters.")
